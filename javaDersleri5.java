package javaDersleri;

public class javaDersleri5 {

	public static void main(String[] args) {
		// For döngüleri
		
		for(int i=1; i<10; i++) {
			//öncelikle i' nin değerini tanımladım sonrasında i'den başlayarak 10'a kadar birer birer artması gerektiğiniz yazdık
			
			System.out.println(i);
		}
			System.out.println("for dongusu bitti");
			
			
			
			
			//while döngüleri
			int i = 10; 
			while(i<10) {
				System.out.println("while dongusu bitti");
				
				i++; //sayinin birer birer artması gerektiğini yazdık
			
			}
			
			//do while dongusu
			int j = 1;
			do {
				 
					System.out.println(j);
					j++;
			}while(i<10);
	}
	


}
